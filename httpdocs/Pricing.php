                                                                <p>PoSP (Point of Sale Person) is a new type of license for insurance agent that was created by IRDAI (Insurance Regulatory Development Authority of India) in 2015. It’s now the best way to enter insurance agent career. Why? Insurance agents are tied to a single company and can sell only products from that one insurer in the given category, i.e. life or non-life. But customers today demand more – they want to know all their options and look for advice from agents on choosing the best product. A PoSP (Point of Sale Person) license or certification lets you do just that. As a PoSP (Point of Sale Person) you can sell policies of multiple insurance companies, across both life and non-life categories, such as term life, ULIP, endowment life, motor, health, personal accident, home and travel. PoSP (Point of Sale Person) is thus a wider concept than a traditional insurance agent license. Therefore most individuals now choose the PoSP (Point of Sale Person) path for agent career.</p>
